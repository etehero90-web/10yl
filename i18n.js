// 기본 언어가 없는 경우 영어 질문/카테고리 상속
Object.keys(I18N).forEach(code => {
  const lang = I18N[code];
  if (lang._base) {
    const base = I18N[lang._base];
    if (!lang.cats) lang.cats = base.cats;
    if (!lang.qs)   lang.qs   = base.qs;
  }
});

